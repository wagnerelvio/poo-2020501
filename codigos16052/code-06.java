// Strings em Java
public class ExemploString {
    public static void main(String[] args) {
        String nome = "Java";

        System.out.println("Tamanho: " + nome.length());
        System.out.println("Maiúsculas: " + nome.toUpperCase());
        System.out.println("Contém 'va'? " + nome.contains("va"));
    }
}
